    public boolean getInput(){
        if(txtSo1.getText().trim().length() == 0|| 
                txtSo2.getText().trim().length() ==0){
            
            JOptionPane.showMessageDialog(this, "Không Được Để Trống");
            txtSo1.requestFocus();
            return false;
        }
        return true;
    }

    
    public void ketQua(){
        
        double so1 = 0;
        double so2 = 0;
        double kQ = 0;
        if (getInput()) {
            try {
                so1 = Double.parseDouble(txtSo1.getText()); 
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "First number phải là số");
                e.printStackTrace();
                return;
            }
            try {
                so2 = Double.parseDouble(txtSo2.getText()); 
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Second number phải là số");
                e.printStackTrace();
                return;
            }
            
            kQ = so1 + so2;
            txtKq.setText(String.valueOf(kQ));
            txtSo1.setText("");
            txtSo2.setText("");
        }
        
    }
    
