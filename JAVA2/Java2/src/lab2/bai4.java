    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if (txtUser.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Khong dc để trống", "Error", JOptionPane.ERROR_MESSAGE);
            return;
            
        }
        
        for (User user : lst) {
            if(user.getUser().equalsIgnoreCase(txtUser.getText())){
                int choice = (JOptionPane.showConfirmDialog(this, "Delete","Confirm", JOptionPane.YES_NO_OPTION));
                if (choice == JOptionPane.YES_OPTION) {
                    lst.remove(user);
                    fillTable();
                    JOptionPane.showMessageDialog(this, "Đã xóa");
                    btnResetActionPerformed(evt);
                    return;
                }else{
                    return;
                }
            }
            
            
        }
        JOptionPane.showMessageDialog(this, "Ngươi dùng không tồn tại");
        btnResetActionPerformed(evt);
        return;
        
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void BtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddActionPerformed
        if (txtUser.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Khong dc để trống", "Error", JOptionPane.ERROR_MESSAGE);
            return;
            
        }
        if (new String(txtpass.getPassword()).equals("")) {
            JOptionPane.showMessageDialog(this, "Khong dc de trong","Error", JOptionPane.WARNING_MESSAGE);
            return;
        } 
        User user = new User();
        
        user.setUser(txtUser.getText());
        user.setPass(new String(txtpass.getPassword()));
        user.setRole(rdbUser.isSelected()?"User": "Admin");
        
        lst.add(user);
        fillTable();
        JOptionPane.showMessageDialog(this, "Đã thêm");
        btnResetActionPerformed(evt);
    }//GEN-LAST:event_BtnAddActionPerformed

    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
        txtUser.setText("");
        txtpass.setText("");
        
    }//GEN-LAST:event_btnResetActionPerformed

    private void tblListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListMouseClicked
        int select = tblList.getSelectedRow();
        if(select>=0){
           String userName =(String) tblmodel.getValueAt(select, 0);
           
            for (User user : lst) {
                if (user.getUser().equals(userName)) {
                    txtUser.setText(user.getUser());
                    txtpass.setText(user.getPass());
                    if(user.getRole().equalsIgnoreCase("Admin")){
                        rdbAdmin.setSelected(true);
                    }else{
                        rdbUser.setSelected(true);
                    }
                }
            }
        }
    }//GEN-LAST:event_tblListMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (txtUser.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Khong dc để trống", "Error", JOptionPane.ERROR_MESSAGE);
            return;
            
        }
        if (new String(txtpass.getPassword()).equals("")) {
            JOptionPane.showMessageDialog(this, "Khong dc de trong","Error", JOptionPane.WARNING_MESSAGE);
            return;
        } 
        for (User user : lst) {
            if (user.getUser().equals(txtUser.getText())) {
                int choice = (JOptionPane.showConfirmDialog(this, "Update ?", "Confirm", JOptionPane.YES_NO_OPTION));
                if (choice == JOptionPane.YES_OPTION){
                    user.setPass(new String(txtpass.getPassword()));
                    if(rdbUser.isSelected()){
                       user.setRole("User");
                    }else{
                        user.setRole("Admin");
                    }
                    fillTable();
                    JOptionPane.showMessageDialog(this, "Đã cập nhật");
                    btnResetActionPerformed(evt);
                    return;
                }else{
                    return;
                }
            }
        }
        
    }//GEN-LAST:event_btnUpdateActionPerformed

