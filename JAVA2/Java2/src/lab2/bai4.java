    private void fillTable(){
        while (tblmodel.getRowCount()>0) {
        tblmodel.removeRow(0);
            
        }
        for (User u : lst) {
            tblmodel.addRow(new String[]{u.getUser(), u.getPass(), u.getRole()});
            
        }
        tblmodel.fireTableDataChanged();
    }
